package org.firstinspires.ftc.teamcode.pathfinding;

public class Vertex {
    public final float x;
    public final float y;

    public Vertex(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}